package Demo_003;

public class Dec_17 {
    public static void main ( String[] args){

       // compound operators

        int a=30;
        a+=30;
        System.out.println(a);
        int b1=a+1;
        System.out.println(b1);
        b1+=1;//b1+1
        System.out.println(b1);
        b1-=1;//b1-1
        System.out.println(b1);
        b1/=2;//b1/2
        System.out.println(b1);
        b1*=4;//b1*4
        System.out.println(b1);




    }
}
